-- ============================================================
-- Joe's Fit — Supabase (PostgreSQL) schema
-- Converted from the original MySQL database.sql
-- Includes: enums, tables, Row-Level Security, an order-tracking
-- RPC, an auth->customer profile trigger, and seed data.
-- ============================================================

-- ---------- Clean slate (makes this script safe to re-run) ----------
-- Drops our objects if they already exist, so running the file twice
-- won't fail with "type ... already exists". This only removes Joe's Fit
-- tables/types — it does not touch Supabase's own schemas.
drop table if exists
  order_history, order_items, reviews, notifications,
  orders, products, categories, customers, staff, coupons cascade;
drop function if exists track_order(text) cascade;
drop function if exists handle_new_user() cascade;
drop function if exists set_updated_at() cascade;
drop type if exists
  payment_method, payment_status, delivery_method,
  order_status, coupon_type, notif_type cascade;

-- ---------- Enum types (MySQL ENUMs -> Postgres types) ----------
create type payment_method  as enum ('cod','gcash','maya','card');
create type payment_status  as enum ('pending','paid','failed','refunded');
create type delivery_method as enum ('standard','express','pickup');
create type order_status    as enum ('pending','confirmed','processing','shipped','delivered','cancelled','returned');
create type coupon_type     as enum ('percent','fixed');
create type notif_type      as enum ('new_order','low_stock','review','payment','other');

-- ---------- updated_at helper ----------
create or replace function set_updated_at()
returns trigger language plpgsql as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

-- ---------- Categories ----------
create table categories (
  id          bigint generated by default as identity primary key,
  name        varchar(100) not null,
  slug        varchar(100) not null unique,
  description text,
  image       varchar(255),
  is_active   boolean default true,
  created_at  timestamptz default now()
);

-- ---------- Products ----------
create table products (
  id          bigint generated by default as identity primary key,
  category_id bigint not null references categories(id) on delete restrict,
  name        varchar(200) not null,
  slug        varchar(200) not null unique,
  description text,
  price       numeric(10,2) not null,
  sale_price  numeric(10,2),
  stock       int default 0,
  sku         varchar(100),
  image       varchar(255),
  images      text,
  sizes       varchar(255),
  colors      varchar(255),
  is_featured boolean default false,
  is_active   boolean default true,
  created_at  timestamptz default now(),
  updated_at  timestamptz default now()
);
create index on products (category_id);
create trigger products_updated_at before update on products
  for each row execute function set_updated_at();

-- ---------- Staff/Admin (managed via Supabase Studio for now) ----------
create table staff (
  id         bigint generated by default as identity primary key,
  name       varchar(100) not null,
  email      varchar(150) not null unique,
  password   varchar(255) not null,
  role       text not null default 'staff' check (role in ('admin','staff')),
  phone      varchar(20),
  avatar     varchar(255),
  is_active  boolean default true,
  last_login timestamptz,
  created_at timestamptz default now()
);

-- ---------- Customers (profile linked to Supabase Auth user) ----------
create table customers (
  id         uuid primary key references auth.users(id) on delete cascade,
  name       varchar(100) not null default '',
  email      varchar(150) not null,
  phone      varchar(20),
  address    text,
  city       varchar(100),
  province   varchar(100),
  zip        varchar(10),
  is_active  boolean default true,
  created_at timestamptz default now()
);

-- ---------- Orders ----------
create table orders (
  id               bigint generated by default as identity primary key,
  tracking_code    varchar(20) not null unique,
  customer_id      uuid references customers(id) on delete set null,
  customer_name    varchar(100) not null,
  customer_email   varchar(150) not null,
  customer_phone   varchar(20),
  shipping_address text not null,
  shipping_city    varchar(100) not null,
  shipping_province varchar(100) not null,
  shipping_zip     varchar(10),
  payment_method   payment_method default 'cod',
  payment_status   payment_status default 'pending',
  delivery_method  delivery_method default 'standard',
  status           order_status default 'pending',
  subtotal         numeric(10,2) not null,
  shipping_fee     numeric(10,2) default 0,
  discount         numeric(10,2) default 0,
  total            numeric(10,2) not null,
  notes            text,
  assigned_to      bigint references staff(id) on delete set null,
  created_at       timestamptz default now(),
  updated_at       timestamptz default now()
);
create index on orders (customer_id);
create trigger orders_updated_at before update on orders
  for each row execute function set_updated_at();

-- ---------- Order Items ----------
create table order_items (
  id            bigint generated by default as identity primary key,
  order_id      bigint not null references orders(id) on delete cascade,
  product_id    bigint references products(id) on delete set null,
  product_name  varchar(200) not null,
  product_image varchar(255),
  size          varchar(20),
  color         varchar(50),
  quantity      int not null default 1,
  price         numeric(10,2) not null,
  subtotal      numeric(10,2) not null
