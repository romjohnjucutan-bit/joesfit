-- Public catalog: anyone can read active categories/products
create policy "public read active categories" on categories
  for select using (is_active = true);
create policy "public read active products" on products
  for select using (is_active = true);

-- Customers: a user can read/update only their own profile
create policy "own profile read"   on customers for select using (auth.uid() = id);
create policy "own profile update" on customers for update using (auth.uid() = id);

-- Orders: a logged-in customer can read their own orders
create policy "own orders read" on orders
  for select using (auth.uid() = customer_id);

-- Order items / history: readable if the parent order belongs to the user
create policy "own order items read" on order_items
  for select using (exists (
    select 1 from orders o where o.id = order_items.order_id and o.customer_id = auth.uid()
  ));
create policy "own order history read" on order_history
  for select using (exists (
    select 1 from orders o where o.id = order_history.order_id and o.customer_id = auth.uid()
  ));

-- Reviews: anyone reads approved reviews; logged-in users can submit (pending approval)
create policy "public read approved reviews" on reviews
  for select using (is_approved = true);
create policy "auth insert reviews" on reviews
  for insert with check (auth.uid() = customer_id);

-- coupons, notifications, staff: no public policies ->
-- only the service_role (Edge Functions / Studio) can touch them.

