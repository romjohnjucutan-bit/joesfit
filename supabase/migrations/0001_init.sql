-- ============================================================
-- AUTH -> CUSTOMER PROFILE
-- Auto-create a customers row whenever a new auth user signs up.
-- ============================================================
create or replace function handle_new_user()
returns trigger language plpgsql security definer set search_path = public as $$
begin
  insert into public.customers (id, email, name)
  values (new.id, new.email, coalesce(new.raw_user_meta_data->>'name', ''))
  on conflict (id) do nothing;
  return new;
end;
$$;

create trigger on_auth_user_created
  after insert on auth.users
  for each row execute function handle_new_user();

-- ============================================================
-- ORDER TRACKING RPC (lets guests look up an order by code only)
-- ============================================================
create or replace function track_order(p_code text)
returns jsonb language plpgsql security definer set search_path = public as $$
declare result jsonb;
begin
  select jsonb_build_object(
    'order', to_jsonb(o) - 'customer_email' - 'customer_phone' - 'shipping_address',
    'items', coalesce((select jsonb_agg(to_jsonb(oi)) from order_items oi where oi.order_id = o.id), '[]'::jsonb),
    'history', coalesce((select jsonb_agg(to_jsonb(oh) order by oh.created_at) from order_history oh where oh.order_id = o.id), '[]'::jsonb)
  )
  into result
  from orders o
  where o.tracking_code = upper(trim(p_code));
  return result;
end;
$$;
grant execute on function track_order(text) to anon, authenticated;

